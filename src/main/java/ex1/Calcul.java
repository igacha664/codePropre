package ex1;

public class Calcul {

	/**
	 * Cette méthide permet d'effectuer une addition
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 *  Cette méthode permet de faire une soustraction
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
